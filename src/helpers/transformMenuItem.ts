export const transformMenuItem = (key: string) => {
  if (key === 'design') return 'Design';
  if (key === 'front_end') return 'Front-end';
  if (key === 'back_end') return 'Back-end';
  if (key === 'full_stack') return 'Full Stack';
  if (key === 'qa_manual') return 'QA Manual';
  if (key === 'pm') return 'PM';
};
